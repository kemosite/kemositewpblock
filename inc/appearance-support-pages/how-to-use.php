<h1>How To Use This Theme</h1>

<hr>

<h2>Header and Footer</h2>
<p>The header makes tools available to allow visiters to "self-serve" the site. Items related to seeking assistance are meant to be placed in the footer.</p>

<hr>

<h2>Customizing Colours</h2>
<p>Custom colour settings are available in the theme settings under "Appearance -> Customize".</p>

<hr>

<h2>MailPoet Integration</h2>
<p>Create additional custom pages to use with MailPoet</p>
<ol>
	<li>Manage Subscriptions Page: You can write any message you want and then paste the shortcode [mailpoet_manage_subscription] onto the page:</li>
	<li>Unsubscribe page (Confirmation and Success): When your subscribers click the "Unsubscribe" link, they will be directed to a confirmation page. These pages must contain the [mailpoet_page] shortcode.</li>
	<li>Re-Engagement Page: Thank your subscribers reactivated by the Re-engagement email for their continued interest in your emails. Page must contain the [mailpoet_page] shortcode.</li>
</ol>

